package com.perfxq.unit.annotation;

import com.perfxq.unit.runner.SecParallelSpringRunner;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Annotation to mark a method as being potentially run concurrently.<br>
 * <br>
 * Created: 23.01.2014 11:27:30
 * 
 * @see SecParallelSpringRunner
 * see also com.google.code.tempusfugit.concurrency.ConcurrentRule
 * @since 2.4.0
 * @author huayq
 */
@Documented
@Target({ METHOD, TYPE })
@Retention(RUNTIME)
public @interface Parallel {
    int count() default 5;
}